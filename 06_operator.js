//Operator in js
console.log("Arithmetic Operator")
let a = 10
let b = 5
console.log("a + b = ",a+b)
console.log("a - b = ",a-b)     
console.log("a * b = ",a*b)
console.log("a / b = ",a/b)
console.log("a % b = ",a%b)
console.log("a ** b = ",a**b)

//After a++ here addition will be done afterward
console.log("a++ = ",a++) //op: 10
console.log("a-- = ",a--) //op: 9

//After ++a here addition will be done before the variable
console.log("++a = ",++a) //op: 11
console.log("--a = ",--a) //op: 10

console.log("Assignment Operator")
let c = 5 
let d = 5

console.log("c = d = ",c=d)
console.log("c += d = ",c+=d)
console.log("c -= d = ",c-=d)
console.log("c *= d = ",c*=d)
console.log("c /= d = ",c/=d)
console.log("c %= d = ",c%=d)
console.log("c **= d = ",c**=d)

let e = "2"
let f = 2

console.log("Comparison Operator")
//check the value 
console.log("e == f = ",e==f)
console.log("e != f = ",e!=f)
//check the value and datatype
console.log("e === f = ",e===f)
console.log("e !== f = ",e!==f)
console.log("e > f = ",e > f)
console.log("e < f = ",e < f)
console.log("e >= f = ",e >= f)
console.log("e <= f = ",e <= f)
console.log("e ?? f = ",e ?? f)

console.log("Logical Operator")
let g = true
let h = false

console.log("g && h = ",g && h)
console.log("g || h = ",g || h)
console.log("!g = ",!g)

